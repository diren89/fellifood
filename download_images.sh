#!/bin/bash
# Downloads recipe images from Unsplash to images/recipes/
cd "$(dirname "$0")"
OUT="images/recipes"

download() {
  local file="$OUT/$1"
  local id="$2"
  if [ ! -f "$file" ]; then
    echo "↓ $1"
    curl -sL "https://images.unsplash.com/photo-${id}?w=400&q=80" -o "$file"
    sleep 0.3
  else
    echo "✓ $1 (already exists)"
  fi
}

